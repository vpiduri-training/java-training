public class BasicsDemoKayla {
	static void print() {
		System.out.print("A long time "); // Print line
		System.out.println("Ago,"); // continue words on the same line as before
		System.out.println(); // Print empty line
		System.out.println("in a galaxy far" + " far" + " away"); // Print several strings on the same line
		System.out.print("Star"); // print string literal on a new line
		System.out.println("Wars"); // Print string literal next to previous without space
		System.out.print("was the "); // print string on a new line
		System.out.print("best"); // print string on the same line
		System.out.println("        "); // print 18 spaces
		System.out.println(); // print empty line
		System.out.println("movie"); // print line and move cursor to the next line
		System.out.print("ever"); // print line
		System.out.println(); // print empty line x1
		System.out.println(); // print empty line x2
		System.out.println(); // print empty line x3
		System.out.println(); // print empty line x4
		System.out.println(); // print empty line x5
		System.out.println(); // print empty line x6
		System.out.println("Luke Skywalker has returned to"); // print line and move cursor to the next line
		System.out.println("his home planet of Tatooine in"); // print line and move cursor to the next line
		System.out.println("an attempt to rescue his"); // print line and move cursor to the next line
		System.out.println("friend Han Solo from the"); // print line and move cursor to the next line
		System.out.println("clutches of the vile gangster"); // print line and move cursor to the next line
		System.out.println("Jabba the Hutt."); // print line and move cursor to the next line
		System.out.println(); // print empty line
	}

	public static void main(String[] args) {
		// Language Basics 1
		print();
	}
}

